#' @title Create a data.frame summarizing MonteCarlo output.
#' @description \code{MonteCarloSummary} returns a data.frame that summarizes the output of \code{MonteCarlo}.
#' @param mc_result Object of class MonteCarlo generated by \code{MonteCarlo}.
#'@import arrayhelpers
#'@export

MonteCarloSummary <- function(mc_result) {
  n_param <- length(mc_result$param_list)
  dim_vec <- numeric(n_param) 
  for(i in 1:n_param){
    dim_vec[i] <- length(mc_result$param_list[[i]])
  }
  grid_size = prod(dim_vec)
  collapse <- list()
  for (i in 1:length(mc_result$results)){
    collapse[[i]] <- "mean"
  }
  collapsed = collapse_results(mc_result$results, collapse=collapse)
  output.name.idx = 1
  for (output.name in names(collapsed)) {
    df = array2df(collapsed[[output.name]], label.x=output.name)
    col.names = names(df)
    param.name.idx = 1
    for (param.name in names(mc_result$param_list)) {
      col.name <- paste("d", param.name.idx, sep="")
      param.mat = matrix(unlist(strsplit(as.character(df[[col.name]]),
                                         "=")),
                         ncol=2, byrow=TRUE)
      param.vals = as.numeric(param.mat[,2])
      df[[col.name]] <- param.vals
      col.name.idx = which(names(df) == col.name)
      col.names[col.name.idx] <- param.name
      param.name.idx = param.name.idx + 1
    }
    names(df) <- col.names
    if (output.name.idx == 1) {
      output.df = df
    }
    else {
      output.df = cbind(output.df, df[,1])
      output.df.names = names(output.df)
      output.df.names[length(output.df.names)] <- col.names[1]
      names(output.df) <- output.df.names
    }
    output.name.idx = output.name.idx + 1
  }
  return(output.df)
}
